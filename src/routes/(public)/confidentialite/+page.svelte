	<h1>Confidentialité & RGPD</h1>
	<p class="warn">
		⚠️ Brouillon v0 — à valider juridiquement avant toute mise en service avec de vrais élèves.
	</p>

	<h2>Responsabilités</h2>
	<p>
		L’établissement scolaire (EPLE) est <strong>responsable de traitement</strong> ; l’association
		Maths974 agit comme <strong>sous-traitant</strong> (accord art. 28 RGPD). Un registre des
		traitements (art. 30) est tenu.
	</p>

	<h2>Données des élèves mineurs</h2>
	<ul>
		<li>Minimisation : aucun e-mail personnel d’élève n’est requis (comptes provisionnés par le professeur).</li>
		<li>Hébergement en Union européenne.</li>
		<li>Aucune publicité, aucun traçage tiers, aucune revente.</li>
		<li>Les identifiants élèves manipulés par les activités sont des pseudonymes opaques.</li>
	</ul>

	<h2>Base légale & consentement</h2>
	<p>
		Le suivi strictement pédagogique relève de la mission de service public de l’établissement. Tout
		traitement allant au-delà (suivi longitudinal, agrégation inter-applications) nécessite une
		information renforcée des familles et, le cas échéant, leur consentement — recueilli et tracé
		avant activation.
	</p>

	<h2>Vos droits</h2>
	<p>
		Accès, rectification, effacement, opposition : adressez votre demande à l’établissement ou à
		l’association. Le droit à l’effacement est techniquement pris en charge.
	</p>
